export default function Component({ 
  onShred,
  isVideoPlaying,
  videoSrc,
  crunchAudioSrc,
  language = "zh",
  onVideoEnded
}: { 
  onShred?: (text: string) => void;
  isVideoPlaying?: boolean;
  videoSrc?: string;
  crunchAudioSrc?: string;
  language?: AppLanguage;
  onVideoEnded?: () => void;
} = {}) {
  const crunchAudioRef = useRef<HTMLAudioElement | null>(null);
  const [inputText, setInputText] = useState("");
  const [hideFlowers, setHideFlowers] = useState(false);
  const [shredderFrame, setShredderFrame] = useState(0);

  useEffect(() => {
    if (!isVideoPlaying) {
      setHideFlowers(false);
    }
  }, [isVideoPlaying]);

  useEffect(() => {
    if (isVideoPlaying) return undefined;

    const interval = window.setInterval(() => {
      setShredderFrame((frame) => (frame + 1) % 2);
    }, 520);

    return () => window.clearInterval(interval);
  }, [isVideoPlaying]);

  const playCrunchAudio = () => {
    if (!inputText.trim()) return;

    const audio = crunchAudioRef.current;
    if (!audio) return;

    audio.currentTime = 0;
    audio.volume = 1;
    audio.play().catch(() => undefined);
  };

  return (
    <div className="bg-white overflow-clip relative rounded-[40px] size-full" data-name="首�?">
      <Frame1 frame={shredderFrame} />
      <button
        onClick={() => {
          playCrunchAudio();
          onShred?.(inputText);
        }}
        className="absolute left-[34px] top-[400px] w-[333px] h-[144px] bg-transparent cursor-pointer z-10"
        aria-label="shred"
      />
      <Frame inputText={inputText} language={language} onTextChange={setInputText} />
      {crunchAudioSrc && <audio ref={crunchAudioRef} src={crunchAudioSrc} preload="auto" />}
      
      {isVideoPlaying && videoSrc && (
        <video
          src={videoSrc}
          autoPlay
          playsInline
          muted
          style={{ backgroundColor: "transparent" }}
          className="absolute inset-0 w-full h-full object-cover z-20 pointer-events-none"
          onEnded={onVideoEnded}
          onTimeUpdate={(e) => {
            if (e.currentTarget.currentTime >= 2) {
              setHideFlowers(true);
            }
          }}
        />
      )}

      <div className="absolute bg-[#dfd4ca] content-stretch flex gap-[40px] items-center justify-center left-[-1px] px-[32px] py-[20px] top-[798px] z-40" data-name="navigation bar">
        <Flower />
        <Calender />
        <Home />
        <Collection />
        <Setting />
      </div>
      
      {!hideFlowers && (
        <SwappingFlowerStrip className="-translate-x-1/2 absolute h-[80px] left-1/2 top-[718px] w-[360px] z-30" />
      )}
    </div>
  );
}
